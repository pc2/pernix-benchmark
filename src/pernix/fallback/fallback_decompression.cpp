#include <benchmark.h>
#include <pernix/pernix.h>


template<uint8_t BIT_WIDTH, bool DISABLE_MEM>
class BenchmarkDecompressorFALLBACK : public BenchmarkDecompressor<BIT_WIDTH, true, DISABLE_MEM> {
public:
    int decompress(const uint8_t *input, const float_t scale, float_t *output) override {
        return pernix::decompress_block_fallback<BIT_WIDTH>(input, scale, output);
    }
};

#define BENCHMARK_DECOMPRESS_BLOCKS_FALLBACK_FUNCTION(N, MEM) \
static void BM_decompress_fallback_##MEM##_##N(benchmark::State& state) { \
BM_decompress_blocks<N, true, MEM, BenchmarkDecompressorFALLBACK<N, MEM>>(state); \
}                                                              \
BENCHMARK_DECOMPRESS_BLOCKS_REGISTER(decompress_fallback_##MEM##_##N);

BENCHMARK_DECOMPRESS_BLOCKS_FALLBACK_FUNCTION(8, true);
