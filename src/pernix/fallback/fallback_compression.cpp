#include <benchmark.h>
#include <libcompression.h>

template<uint8_t BIT_WIDTH, bool DISABLE_MEM>
class BenchmarkCompressorFALLBACK : public BenchmarkCompressor<BIT_WIDTH, DISABLE_MEM> {
public:
    int compress(const float_t *input, const float_t scale, uint8_t *output) override {
        return libcompression::compress_block_fallback<BIT_WIDTH>(input, scale, output);
    }
};

#define BENCHMARK_COMPRESS_BLOCKS_FALLBACK_FUNCTION(N, MEM) \
static void BM_compress_fallback_##MEM##_##N(benchmark::State& state) { \
BM_compress_blocks<N, true, MEM, BenchmarkCompressorFALLBACK<N, MEM>>(state); \
}                                                              \
BENCHMARK_COMPRESS_BLOCKS_REGISTER(compress_fallback_##MEM##_##N);

BENCHMARK_COMPRESS_BLOCKS_FALLBACK_FUNCTION(8, true);
BENCHMARK_COMPRESS_BLOCKS_FALLBACK_FUNCTION(9, true);
BENCHMARK_COMPRESS_BLOCKS_FALLBACK_FUNCTION(10, true);
BENCHMARK_COMPRESS_BLOCKS_FALLBACK_FUNCTION(11, true);
BENCHMARK_COMPRESS_BLOCKS_FALLBACK_FUNCTION(12, true);
BENCHMARK_COMPRESS_BLOCKS_FALLBACK_FUNCTION(13, true);
BENCHMARK_COMPRESS_BLOCKS_FALLBACK_FUNCTION(14, true);
BENCHMARK_COMPRESS_BLOCKS_FALLBACK_FUNCTION(15, true);
BENCHMARK_COMPRESS_BLOCKS_FALLBACK_FUNCTION(16, true);

